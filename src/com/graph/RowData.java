package com.graph;

public class RowData {
	 String col1Value;
	 String col2Value;
	public RowData(String col1Value, String col2Value) {
		super();
		this.col1Value = col1Value;
		this.col2Value = col2Value;
	}
	public String getCol1Value() {
		return col1Value;
	}
	public String getCol2Value() {
		return col2Value;
	}
	public RowData(){
		super();
	}
	
	
}
